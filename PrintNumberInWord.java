
public class PrintNumberInWord {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		int number = 30;
				if(number == 1){
					System.out.println("ONE");
					
				}else if (number == 2){
					System.out.println("TWO");
					
				}else if (number == 3){
					System.out.println("THREE");
					
				}else if (number == 4){
					System.out.println("FOUR");
			}else{
					System.out.println("OTHER");
				
				}
				

	}

}
