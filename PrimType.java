
public class PrimType {

	public static void main(String[] args) {
		// Numeric primitive data type
        int integer = 2898;
        long longInteger = 2442222L;
        short shortInteger = 3424;
        byte byteInteger = 10;
        // Floating point type real numbers
        double doubleFloatNumber = 0.5;
        float floatNumber = 0.244424f;
        // Characters
        char charSymbol = 'a';
        char charSymbolAsNumber = 83;
        char charSymbolAsNumber1 = 65;
        char charSymbolAsNumber2 = 78;
        // Booleans
        boolean booleanTrueFalse = true;
        
        System.out.println(""+charSymbol+charSymbolAsNumber  + charSymbolAsNumber1  +  charSymbolAsNumber2);
        System.out.println(booleanTrueFalse);
	}

}
